# Bài 10: Diện tích căn phòng

# Nhập chiều dài và chiều rộng (float)
length = float(input("Nhập chiều dài (m): "))
width = float(input("Nhập chiều rộng (m): "))

# Tính diện tích
area = length * width

print("Diện tích căn phòng là:", area, "m²")


